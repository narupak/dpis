	function print_header(){
		global $worksheet, $xlsRow, $heading_name;
		global $ARR_LEVEL_GROUP, $ARR_LEVEL_GROUP_NAME, $ARR_LEVEL, $ARR_LEVEL_NAME;
		global $arr_column_map, $arr_column_sel, $arr_column_align, $arr_column_width;
